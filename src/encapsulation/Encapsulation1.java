package encapsulation;

public class Encapsulation1 {

	private String name;
	private int age;

	// Setting Name value
	public void setName(String userName) {
		name = userName;

	}

	// Setting Age value
	public void setAge(int userAge) {
		age = userAge;

	}

	// Getting Name value
	public String getName() {
		return name;

	}

	// Getting Age value
	public int getAge() {
		return age;

	}

}
